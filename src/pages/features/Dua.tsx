export function DuaView({ onBack, initialDuas, title: customTitle }: { onBack: () => void, initialDuas?: Dua[], title?: string }) {
  const { t } = useLanguage();
  const [searchQuery, setSearchQuery] = useState('');
  const [selectedDua, setSelectedDua] = useState<Dua | null>(null);
  const [copied, setCopied] = useState(false);
  const { toggleFavorite, isFavorite } = useFavorites<Dua>('favorite-duas');
  const isMobile = Capacitor.isNativePlatform() || (typeof window !== 'undefined' && window.innerWidth < 768);

  useEffect(() => {
    if (selectedDua) {
      window.history.pushState({ dua: selectedDua.id }, '', '');
      
      const handlePopState = () => {
        setSelectedDua(null);
      };
      
      window.addEventListener('popstate', handlePopState);
      return () => window.removeEventListener('popstate', handlePopState);
    }
  }, [selectedDua]);

  const displayDuas = initialDuas || DUA_LIST;

  const filteredDuas = displayDuas.filter(dua => 
    dua.title.toLowerCase().includes(searchQuery.toLowerCase())
  );

  const handleCopy = async (text: string) => {
    try {
      await navigator.clipboard.writeText(text);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      console.error('Failed to copy text: ', err);
    }
  };

  const handleShare = async (dua: Dua) => {
    let text = `${dua.title}\n${dua.subtitle ? dua.subtitle + '\n' : ''}\n`;
    
    if (dua.sections) {
      text += dua.sections.map(s => {
        if (s.type === 'dua') return `${s.title ? s.title + '\n' : ''}${s.arabic}\n${s.translation}\n${s.reference || ''}`;
        if (s.type === 'text') return s.content;
        if (s.type === 'heading' || s.type === 'subheading') return s.title;
        return '';
      }).join('\n\n');
    } else {
      text += `${dua.arabic}\n\n${dua.translation}\n\n${dua.reference || ''}`;
    }

    if (navigator.share) {
      try {
        await navigator.share({
          title: dua.title,
          text: text,
        });
      } catch (error) {
        console.error('Error sharing:', error);
      }
    } else {
      handleCopy(text);
    }
  };

  if (selectedDua) {
    return (
      <motion.div 
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        className="flex flex-col min-h-full bg-white dark:bg-slate-950 pb-8"
      >
        {/* Detail Header */}
        <header className={cn(
          "sticky top-0 z-10 bg-white dark:bg-slate-950 px-4 pb-2 flex items-center justify-between",
          "pt-safe"
        )}>
          <div className="flex items-center gap-4">
            {!isMobile && (
              <button 
                onClick={() => setSelectedDua(null)}
                className="p-1.5 hover:bg-slate-100 dark:hover:bg-slate-900 rounded-full transition-colors"
              >
                <ArrowLeft className="w-5 h-5 text-slate-700 dark:text-slate-300" />
              </button>
            )}
          </div>
          <div className="flex gap-1">
            <button 
              onClick={() => toggleFavorite(selectedDua)}
              className={cn(
                "p-1.5 transition-colors",
                isFavorite(selectedDua.id) ? "text-primary" : "text-slate-400 hover:text-primary"
              )}
            >
              {isFavorite(selectedDua.id) ? <BookmarkCheck className="w-4 h-4" /> : <BookmarkPlus className="w-4 h-4" />}
            </button>
            <button 
              onClick={() => handleShare(selectedDua)}
              className="p-1.5 text-slate-400 hover:text-primary transition-colors"
            >
              <Share2 className="w-4 h-4" />
            </button>
          </div>
        </header>

        <div className="flex-1 px-4 py-0 space-y-3">
          {/* Title Section */}
          <div className="text-center space-y-0">
            <h2 className="text-base font-bold text-slate-800 dark:text-white">
              {selectedDua.title}
            </h2>
            {selectedDua.subtitle && (
              <p className="text-xs font-medium text-slate-500 dark:text-slate-400">
                {selectedDua.subtitle}
              </p>
            )}
          </div>

          {selectedDua.sections ? (
            <div className="space-y-4 pb-6">
              {selectedDua.sections.map((section, idx) => (
                <div key={idx} className="space-y-2">
                  {section.type === 'heading' && (
                    <h3 className="text-sm font-bold text-primary dark:text-primary-light border-b border-primary-light/30 dark:border-primary-dark/30 pb-1 mt-4">
                      {section.title}
                    </h3>
                  )}
                  {section.type === 'subheading' && (
                    <h4 className="text-xs font-bold text-slate-700 dark:text-slate-300 mt-3">
                      {section.title}
                    </h4>
                  )}
                  {section.type === 'text' && (
                    <p className="text-[13px] text-slate-600 dark:text-slate-400 leading-relaxed whitespace-pre-line">
                      {section.content}
                    </p>
                  )}
                  {section.type === 'list' && (
                    <ul className="space-y-1">
                      {section.items?.map((item, i) => (
                        <li key={i} className="text-[13px] text-slate-600 dark:text-slate-400 leading-relaxed">
                          {item}
                        </li>
                      ))}
                    </ul>
                  )}
                  {section.type === 'dua' && (
                    <div className="space-y-2 pt-2">
                      <div className="flex items-center justify-between">
                        {section.title && <p className="text-[11px] font-bold text-primary uppercase tracking-tighter">{section.title}</p>}
                        <button 
                          onClick={() => handleCopy(`${section.arabic}${section.arabicAlternative ? '\nঅথবা,\n' + section.arabicAlternative : ''}\n\n${section.translation}\n\n${section.reference || ''}`)}
                          className="p-1 text-slate-400 hover:text-primary-500 transition-colors"
                          title="কপি করুন"
                        >
                          <Copy className="w-3.5 h-3.5" />
                        </button>
                      </div>
                      {section.intro && <p className="text-[13px] text-slate-600 dark:text-slate-400 italic">{section.intro}</p>}
                      <div className="space-y-3">
                        <p className="text-2xl font-arabic text-slate-800 dark:text-slate-100 leading-[1.6] dir-rtl text-center">
                          {section.arabic}
                        </p>
                        <p className="text-[14px] font-medium text-slate-700 dark:text-slate-300 leading-snug text-center pt-2">
                          {section.translation}
                        </p>
                        {section.reference && (
                          <p className="text-[10px] text-slate-400 text-center italic">{section.reference}</p>
                        )}
                      </div>
                    </div>
                  )}
                </div>
              ))}
              
              {/* Copy All Button */}
              <div className="flex justify-center pt-4 pb-8">
                <button 
                  onClick={() => {
                    const allText = selectedDua.sections?.map(s => {
                      if (s.type === 'dua') return `${s.title ? s.title + '\n' : ''}${s.arabic}\n${s.translation}\n${s.reference || ''}`;
                      if (s.type === 'text') return s.content;
                      if (s.type === 'heading' || s.type === 'subheading') return s.title;
                      return '';
                    }).join('\n\n');
                    handleCopy(allText || '');
                  }}
                  className="flex items-center gap-2 px-4 py-2 bg-primary text-white rounded-xl text-xs font-bold shadow-lg shadow-primary/20 hover:bg-primary-dark transition-all"
                >
                  {copied ? <Check className="w-4 h-4" /> : <Copy className="w-4 h-4" />}
                  {copied ? 'সবগুলো কপি হয়েছে' : 'সবগুলো কপি করুন'}
                </button>
              </div>
            </div>
          ) : selectedDua.arabic ? (
            <div className="space-y-3">
              {/* Dua Label */}
              <div className="text-center">
                <span className="px-2 py-0.5 bg-primary-light/20 dark:bg-primary-dark/20 text-primary dark:text-primary-light rounded-full text-[9px] font-bold uppercase tracking-wider">
                  দুআ
                </span>
              </div>

              {/* Arabic Section */}
              <div className="space-y-4 text-center">
                <p className="text-2xl font-arabic text-slate-800 dark:text-slate-100 leading-[1.6] dir-rtl">
                  {selectedDua.arabic}
                </p>
              </div>

              {/* Translation Section */}
              <div className="pt-2 border-t border-slate-100 dark:border-slate-800">
                <p className="text-[14px] font-medium text-slate-700 dark:text-slate-300 leading-snug text-center">
                  {selectedDua.translation}
                </p>
              </div>

              {/* Reference Section */}
              {selectedDua.reference && (
                <p className="text-[10px] text-slate-400 italic text-center pt-1">
                  {selectedDua.reference}
                </p>
              )}

              {/* Action Buttons */}
              <div className="flex justify-center gap-2 pt-4 pb-4">
                <button 
                  onClick={() => handleCopy(`${selectedDua.arabic}\n\n${selectedDua.translation}\n\n${selectedDua.reference || ''}`)}
                  className="flex items-center gap-1.5 px-3 py-1.5 bg-slate-100 dark:bg-slate-900 text-slate-600 dark:text-slate-300 rounded-lg text-[10px] font-bold hover:bg-primary hover:text-white dark:hover:bg-primary-dark transition-all"
                >
                  {copied ? <Check className="w-3 h-3" /> : <Copy className="w-3 h-3" />}
                  {copied ? t('copied') : t('copy')}
                </button>
              </div>
            </div>
          ) : (
            <div className="flex flex-col items-center justify-center py-20 text-slate-400">
              <BookmarkPlus className="w-12 h-12 mb-4 opacity-20" />
              <p className="text-lg font-bold">{t('dua-details-soon')}</p>
            </div>
          )}
        </div>
      </motion.div>
    );
  }

  return (
    <div className="flex flex-col min-h-full bg-white dark:bg-slate-950">
      {/* Header */}
      <header className={cn(
        "sticky top-0 z-10 bg-white dark:bg-slate-900 border-b border-slate-100 dark:border-slate-800 px-4 pb-4 flex items-center gap-4",
        "pt-safe"
      )}>
        {!isMobile && (
          <button 
            onClick={onBack}
            className="p-2 hover:bg-slate-50 dark:hover:bg-slate-800 rounded-full transition-colors"
          >
            <ArrowLeft className="w-6 h-6 text-slate-700 dark:text-slate-300" />
          </button>
        )}
        <h1 className="text-xl font-bold text-slate-800 dark:text-white">{customTitle || t('dua-daily-life')}</h1>
      </header>

      {/* Search Bar */}
      <div className="px-4 py-3 bg-slate-50 dark:bg-slate-900/50">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-slate-400" />
          <input
            type="text"
            placeholder={t('search-dua')}
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
            className="w-full pl-10 pr-4 py-2 bg-white dark:bg-slate-800 border border-slate-200 dark:border-slate-700 rounded-xl text-sm focus:outline-none focus:ring-2 focus:ring-primary/20 transition-all"
          />
        </div>
      </div>

      {/* Dua List */}
      <div className="flex-1">
        <AnimatePresence>
          {filteredDuas.map((dua, index) => (
            <motion.div
              key={dua.id}
              initial={{ opacity: 0, y: 10 }}
              animate={{ opacity: 1, y: 0 }}
              exit={{ opacity: 0, scale: 0.95 }}
              transition={{ delay: index * 0.02 }}
              onClick={() => setSelectedDua(dua)}
              className="flex items-center justify-between px-4 py-4 border-b border-slate-50 dark:border-slate-900 hover:bg-slate-50 dark:hover:bg-slate-900/50 transition-colors cursor-pointer group"
            >
              <span className="text-[15px] font-medium text-slate-700 dark:text-slate-300 leading-snug flex-1 pr-4">
                {dua.title}
              </span>
              <button 
                onClick={(e) => {
                  e.stopPropagation();
                  toggleFavorite(dua);
                }}
                className={cn(
                  "p-2 transition-colors",
                  isFavorite(dua.id) ? "text-primary" : "text-slate-400 hover:text-primary dark:hover:text-primary-light"
                )}
              >
                {isFavorite(dua.id) ? <BookmarkCheck className="w-5 h-5" /> : <BookmarkPlus className="w-5 h-5" />}
              </button>
            </motion.div>
          ))}
        </AnimatePresence>
        
        {filteredDuas.length === 0 && (
          <div className="flex flex-col items-center justify-center py-20 text-slate-400">
            <Search className="w-12 h-12 mb-4 opacity-20" />
            <p className="text-sm">{t('no-dua-found')}</p>
          </div>
        )}
      </div>
    </div>
  );
}
